function createPerformanceFig_CoordFrameProj

global h
              
    % Create figure
    h.performanceF = figure('NumberTitle',    'off',...
                             'name',           'Performance (no trials yet completed)',...
                             'color',          'w',...
                             'units',          'centimeters',...
                             'position',       [20.8 2.5 10 8],...
                             'MenuBar',        'none',...
                             'KeyPressFcn',    @KeyPress);    
    
    % Create axes
    h.performanceA = axes;
    
    % Create image and color bar
    h.im = imagesc(zeros(12,12));
    h.im = repmat(h.im, 1, 4);
    colormap(magma)
    cbar = colorbar;
    
    % Format exes
    axis square
    xlabel('Target Location')
    ylabel('Response Location')
    ylabel(cbar,'N Trials','FontSize',8)
    title('Performance')
            
    set(h.performanceA, 'FontSize',     8,...
        'FontName',     'arial',...
        'color',        'none',...
        'xcolor',       'k',...
        'xdir',         'normal',...
        'ycolor',       'k');